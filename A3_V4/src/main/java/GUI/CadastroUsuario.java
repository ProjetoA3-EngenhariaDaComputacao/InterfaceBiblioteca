    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        if (!"".equals(txtNome.getText()))
        {
            Pessoa ps = new Pessoa();
           
            ps.setCpf(txtCPF.getText());
            ps.setNome(txtNome.getText());
            ps.setIdade(txtIdade.getText());
            ps.setSexo(txtSexo.getText());
            ps.setEmail(txtEmail.getText());
            ps.setSenha(txtSenha.getText());
            ps.setTipoUsu(txtTipoUsu.getText());
            
            ps.apagar();
            Limpar(); 
            System.out.println("Apagado com sucesso!!");
        }
        else
        {
            JOptionPane.showMessageDialog
                    (null,"Campos em branco, complete-os para poder Excluir!!",
                            "Cadastro de Usu치rio", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btExcluirActionPerformed

