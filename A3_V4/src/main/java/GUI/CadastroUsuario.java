    private void btIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIncluirActionPerformed
        // TODO add your handling code here:
        
        if (!"".equals(txtNome.getText()))
        {
            Usuario usu = new Usuario();
           
            usu.setCpf(txtCPF.getText());
            usu.setNome(txtNome.getText());
            usu.setIdade(txtIdade.getText());
            usu.setSexo(txtSexo.getText());
            usu.setEmail(txtEmail.getText());
            usu.setSenha(txtSenha.getText());
            usu.setTipoUsu(txtTipoUsu.getText());
            
        try {
          usu.inserir();          
          Limpar();   
          System.out.println("Atualizado com sucesso!!");
        } catch (SQLException ex) {
            Logger.getLogger(CadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }  
        }
        else
        {
            JOptionPane.showMessageDialog
                    (null,"Campos em branco, complete-os para poder incluir!!",
                            "Cadastro de Usu치rio", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btIncluirActionPerformed

